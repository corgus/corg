            <div class="port_left">
                <p>Current audio editing software, such as Ableton Live or Apple's Garageband, tend to isolate each recording or instrument into physically separate entities. This creates a segmented approach to development, though the finished product is typically a single waveform. To dissolve this segmentation, I developed a new approach that caters to the visually thinking sound sculpter.</p> 
                <p>Written in MATLAB, this program plots individual audio files on a single plane. The horizontal axis determines output chronology, but the vertical axis can have any number of applications. </p>
            </div>
            <div class="port_right">
            	<p>A user might organize files vertically based on pitch, timbre, recording date, musician, or any other means. Groups may be formed to simplify the canvas, and guides for tempo or time signature can be activated.</p>
                <p>The signal processing libraries of MATLAB are second to none, but accessability of this program is largely limited to those in higher education, or with a deep wallet. I hope to return to this project in the future, possibly using a canvas with HTML5, to tap into the much larger creative sphere of websurfing audiogeeks.</p>
